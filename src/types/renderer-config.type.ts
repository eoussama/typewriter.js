
/**
   * @description
   * Caret configuration
   */
export interface IRendererConfig {

  /**
   * @description
   * Whether of the caret is enabled
   */
  enable: boolean,

  /**
   * @description
   * Whether blinking is enabled
   */
  blink: boolean,

  /**
   * @description
   * The content of the caret
   */
  content: string
}
